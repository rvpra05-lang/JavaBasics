package Module1.Programs;

public class IfElse
{
	public static void main(String[]args)
	{
		int a=5;
		int b=a+100;
		int c=100-a;
		if(a>=5)
		{
			
			System.out.println(b);
		}
		else
		{
		
			System.out.println(c);
		}
		int a1=3;
		int b1=a1+100;
		int c1=100-a1;
		if(a1>=5)
		{
			
			System.out.println(b1);
		}
		else
		{
		
			System.out.println(c1);
		}
	}

}
